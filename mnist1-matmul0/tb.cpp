template<int IC, int OC>
void matmul(
    const int (&input)[IC],
    const int (&weight)[OC*IC],
    int (&output)[OC]
) {
    for (int oc = 0; oc < OC; oc++) {
        int sum = 0;
        for (int ic = 0; ic < IC; ic++) {
            sum += input[ic] * weight[oc * IC + ic];
        }
        output[oc] = sum;
    }
}

template<int N, int C>
bool compare(
    int (&x)[N][C],
    int (&y)[N][C]
) {
    bool match = true;
    for (int i = 0; i < N; i++) {
        for (int c = 0; c < C; c++) {
            if (x[i][c] != y[i][c]) {
                printf("mismatch : (%d, %d) %d != %d\n", i, c, x[i][c], y[i][c]);
                match = false;
                break;
            }
        }
    }
    return match;
}

