extern uint32_t images[20][28];
extern long conv0_wi[];
extern long conv0_thr[];
extern long conv1_wi[];
extern long conv1_thr[];
extern long mat0_wi[];

int labels[20] = {
    7, 2, 1, 0, 4, 1, 4, 9, 5, 9, 0, 6, 9, 0, 1, 5, 9, 7, 3, 4, 
};

void vector_to_stream(const int input[], hls::stream<long>& ins) {
    for (int i = 0; i < 28 * 28; i++) {
        ins.write(input[i]);
    }

    for (int i = 0; i < 16 * 5 * 5; i++) {
        ins.write(conv0_wi[i]);
    }
    for (int i = 0; i < 16 * 7; i++) {
        ins.write(conv0_thr[i]);
    }

    for (int i = 0; i < 16 * 5 * 5; i++) {
        ins.write(conv1_wi[i]);
    }
    for (int i = 0; i < 16 * 7; i++) {
        ins.write(conv1_thr[i]);
    }

    for (int i = 0; i < 10 * 256 / 16; i++) {
        ins.write(mat0_wi[i]);
    }
}

int main(int argc, char** argv)
{
    const int N = 20;

    int input[N][28*28*1];
    for (int i = 0; i < N; i++) {
        for (int y = 0; y < 28; y++) {
            auto v = images[i][y];
            for (int x = 0; x < 28; x++) {
                input[i][y * 28 + x] = (v >> (27 - x)) & 1;
            }
        }
    }

    int out[N][1];
    for (int i = 0; i < N; i++) {
        hls::stream<long> ins;
        vector_to_stream(input[i], ins);
        kernel(ins, out[i]);
    }

    int match_count = 0;
    for (int i = 0; i < N; i++) {
        printf("kernel output = %d, answer = %d\n", out[i][0], labels[i]);
        if (out[i][0] == labels[i]) match_count++;
    }

    int accuracy = 100 * match_count / N;
    printf("accuracy = %d%%\n", accuracy);

    if (accuracy < 90)
        return EXIT_FAILURE;
}

