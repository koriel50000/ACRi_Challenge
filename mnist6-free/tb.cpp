int main(int argc, char** argv)
{
    const int N = 20;

    int input[N][28*28*1];
    for (int i = 0; i < N; i++) {
        for (int y = 0; y < 28; y++) {
            auto v = images[i][y];
            for (int x = 0; x < 28; x++) {
                input[i][y * 28 + x] = (v >> (27 - x)) & 1;
            }
        }
    }

    int out[N][1];
    for (int i = 0; i < N; i++) {
        hls::stream<uint64_t> ins;
        vector_to_stream(input[i], ins);
        kernel(ins, out[i]);
    }

    int match_count = 0;
    for (int i = 0; i < N; i++) {
        printf("kernel output = %d, answer = %d\n", out[i][0], labels[i]);
        if (out[i][0] == labels[i]) match_count++;
    }

    int accuracy = 100 * match_count / N;
    printf("accuracy = %d%%\n", accuracy);

    if (accuracy < 90)
        return EXIT_FAILURE;
}

