void affine2(int out[1], fifo<T>& outs) {
	static T w2[FL * CL] = {
H(385a,b833,3778,935d,31d8,b7a3,3f74,bc97,b8e0,b591),
H(3842,217d,3a79,307c,b406,3b9a,b421,38ef,bc28,be47),
H(bb57,b61a,3c56,393a,a6c8,39b3,ba01,b458,ba99,399e),
H(3db6,bc14,3132,b819,b823,3be5,3116,bbcc,39e1,b762),
H(b8fb,b3df,bec7,311d,3a3d,aac7,b1b9,b429,3a12,3d8e),
H(b88e,3dc9,aea2,b743,b9d5,3d6f,3460,38af,bb0f,bb4f),
H(bcda,3f99,393a,30f8,bc41,b6eb,2ce1,3728,30d3,b96d),
H(382f,b96d,3ab2,3edb,bc24,3a80,bd0c,b9cf,2013,b3c1),
H(2fac,bbdb,3473,b125,ac31,bc91,b9f7,402e,b031,39c0),
H(b828,b769,38f7,b426,3e2f,b484,37e3,bbab,360e,b869),
	};
#pragma HLS array_partition variable=w2 cyclic factor=CL

	static T acc[CL];
#pragma HLS array_partition variable=acc

	for (int i = 0; i < CL; i++) {
#pragma HLS unroll
		acc[i] = 0;
	}

	for (int j = 0; j < FL; j++) {
#pragma HLS pipeline
		T v = outs.read();
		if (v > 0) {
			for (int i = 0; i < CL; i++) {
#pragma HLS unroll
				T w = w2[j * CL + i];
				acc[i] += v * w;
			}
		}
	}

	T max = acc[0];
	int m = 0;
	for (int i = 1; i < CL; i++) {
#pragma HLS pipeline
		T v = acc[i];
		if (v > max) {
			max = v;
			m = i;
		}
	}
	out[0] = m;
}

void kernel(int in[H * W], int out[1]) {
#pragma HLS interface axis port=in
#pragma HLS interface axis port=out
//#pragma HLS array_partition variable=in cyclic factor=W

	fifo<T> ios("fifo");

#pragma HLS dataflow
	affine1(in, ios);
	affine2(out, ios);
}
