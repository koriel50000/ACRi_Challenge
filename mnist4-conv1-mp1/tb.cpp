template<int KH, int KW, int IH, int IW, int IC, int OH, int OW, int OC, int M>
void conv(
    int (&in)[IH*IW*IC],
    int (&weight)[OC*KH*KW*IC],
    int (&threshold)[M],
    int (&out)[OH*OW*OC]
) {
    for (int y = 0; y < OH; y++) {
        for (int x = 0; x < OW; x++) {
            for (int oc = 0; oc < OC; oc++) {
                int sum = 0;
                for (int ky = 0; ky < KH; ky++) {
                    for (int kx = 0; kx < KW; kx++) {
                        for (int ic = 0; ic < IC; ic++) {
                            sum += in[((y + ky) * IW + x + kx) * IC + ic] * weight[((oc * KH + ky) * KW + kx) * IC + ic];
                        }
                    }
                }
                int o = 0;
                for (int i = 0; i < M; i++) {
                    if (sum >= threshold[i]) {
                        o = i + 1;
                    }
                }
                out[(y * OW + x) * OC + oc] = o;
            }
        }
    }
}

template<
    int KH, int KW, int IH, int IW, int C,
    int OH = IH / 2, int OW = IW / 2
>
void maxpool(
    int (&in)[IH*IW*C],
    int (&out)[OH*OW*C]
) {
    for (int y = 0; y < OH; y++) {
        for (int x = 0; x < OW; x++) {
            for (int c = 0; c < C; c++) {
                auto max = std::numeric_limits<int>::min();
                for (int ky = 0; ky < KH; ky++) {
                    for (int kx = 0; kx < KW; kx++) {
                        auto v = in[((y * KH + ky) * IW + x * KW + kx) * C + c];
                        if (max < v) max = v;
                    }
                }
                out[(y * OW + x) * C + c] = max;
            }
        }
    }
}

template<int N, int C>
bool compare(
    int (&x)[N][C],
    int (&y)[N][C]
) {
    bool match = true;
    for (int i = 0; i < N; i++) {
        for (int c = 0; c < C; c++) {
            if (x[i][c] != y[i][c]) {
                printf("mismatch : (%d, %d) %d != %d\n", i, c, x[i][c], y[i][c]);
                match = false;
                break;
            }
        }
    }
    return match;
}

